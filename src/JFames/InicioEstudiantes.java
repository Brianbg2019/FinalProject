    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        int resultado = 0;
        
        try {
            String nie = txtNieBrowser.getText();
            
            String sql = "SELECT * FROM estudiantes WHERE nie='"+nie+"'";
            
            Statement st = conect.createStatement();
            ResultSet rs = st.executeQuery(sql);
            
            if (rs.next()) {
                resultado = 1;
                
                if (resultado == 1) {
                    JOptionPane.showMessageDialog(null, "Bienvenid@!");
                    System.exit(0);
                }
            }else{
                JOptionPane.showMessageDialog(null, "Su nie es invalido");
                limpiar();
            }
        } catch (Exception e) {
            System.err.println("Error en la consultta" + e);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

